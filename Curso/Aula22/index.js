/* 
operadores lógicos
&& -> AND -> E
|| -> OR -> OU
! -> NOT -> NÃO
*/

console.log(true && true); // true vai retornar quando todas são verdadeiras
console.log(true && false); 
console.log(true || false); // true quando alguma expressão voltar verdadeira
console.log(false || false);

const not = !true; // retorna o inverso do resultado, se for verdadeiro vai retornar falso

console.log(not);